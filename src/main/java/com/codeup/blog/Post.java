package com.codeup.blog;

public class Post {
    private String title;
    private String body;

    public Post(){
        this.title = title;
        this.body = body;
    }

    public void setTitle() {
        this.title = title;
    }

    public String getTitle(){
        return title;
    }

    public void setBody() {
        this.body = body;
    }

    public String getBody() {
        return body;
    }
}
